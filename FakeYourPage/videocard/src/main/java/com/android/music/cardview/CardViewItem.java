package com.android.music.cardview;

public class CardViewItem {
	private String mVideoId;
	private String mTitle;
	private String mSonTitle;
	private String mPicUrl;

	public String getVideoId() {
		return mVideoId;
	}

	public void setVideoId(String mVideoId) {
		this.mVideoId = mVideoId;
	}

	public String getTitle() {
		return mTitle;
	}

	public void setTitle(String mTitle) {
		this.mTitle = mTitle;
	}

	public String getSonTitle() {
		return mSonTitle;
	}

	public void setSonTitle(String mSonTitle) {
		this.mSonTitle = mSonTitle;
	}

	public String getPicUrl() {
		return mPicUrl;
	}

	public void setPicUrl(String mPicUrl) {
		this.mPicUrl = mPicUrl;
	}

}
